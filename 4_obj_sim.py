# 3. ANIMATION SETUP
# ------------------------------------------------------------
env = DynamicEnv(size=10)

fig, ax = plt.subplots(figsize=(6, 6))
ax.set_xlim(-1, env.size)
ax.set_ylim(-1, env.size)
ax.set_xticks(np.arange(env.size))
ax.set_yticks(np.arange(env.size))
ax.grid(True)

robot_plot, = ax.plot([], [], 'bo', markersize=10, label="Robot")
goal_plot, = ax.plot(env.goal[0], env.goal[1], 'go', markersize=12, label="Goal")
# obstacle main markers (continuous, red squares)
obs_plots = [ax.plot([], [], marker='s', linestyle='None', markersize=10)[0] for _ in env.obstacles]
# predicted next positions (smaller yellow markers)
pred_plots = [ax.plot([], [], marker='s', linestyle='None', markersize=6)[0] for _ in env.obstacles]

plt.legend(loc="upper left")

def init():
    robot_plot.set_data([], [])
    for op in obs_plots:
        op.set_data([], [])
    for pp in pred_plots:
        pp.set_data([], [])
    return [robot_plot] + obs_plots + pred_plots

def update(frame):
    action = choose_action(env)
    next_state, reward, done = env.step(action)

    # Plot robot (grid position)
    robot_plot.set_data([next_state[0]], [next_state[1]])

    # Update obstacles (continuous positions)
    for i, obs in enumerate(env.obstacles):
        ox, oy, _, _, _, _ = obs
        obs_plots[i].set_data([ox], [oy])

    # Update predicted obstacle positions (one-step forecast)
    preds = env.predict_obstacles(steps=1)
    for i, p in enumerate(preds):
        pred_plots[i].set_data([p[0]], [p[1]])

    if done:
        ani.event_source.stop()

    return [robot_plot] + obs_plots + pred_plots

ani = animation.FuncAnimation(
    fig,
    update,
    init_func=init,
    frames=1000,
    interval=200,
    blit=True
)

# DISPLAY ANIMATION IN JUPYTER / COLAB
animation_html = ani.to_jshtml()
HTML(animation_html)